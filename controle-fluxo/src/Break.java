public class Break {
    public static void main(String[] args) {
        for(int numero = 1; numero<= 5; numero ++){
            if (numero == 3)
            break; //pode ser susbtituido por continue, que realiza e continua a instrução demandada na condição if definida anteriormente.


            System.out.println(numero);
        }
        
    }
}
